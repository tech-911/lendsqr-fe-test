export const minCardColor = (color: string) => {
  switch (color) {
    case "1":
      return "#FFC700";
    case "2":
      return "#FF3D57";
    case "3":
      return "#0081FF";
    case "4":
      return "#008124";
    case "5":
      return "#561F37";
    default:
      return "#ED7D3A";
  }
};
